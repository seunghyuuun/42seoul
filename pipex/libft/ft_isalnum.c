int	ft_isalnum(int c)
{
	if (c < '0')
		return (0);
	else if (c > '9' && c < 'A')
		return (0);
	else if (c > 'Z' && c < 'a')
		return (0);
	else if (c > 'z')
		return (0);
	return (1);
}
